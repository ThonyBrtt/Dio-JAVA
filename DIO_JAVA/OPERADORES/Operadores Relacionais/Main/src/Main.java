public class Main {
    public static void main(String[] args) {
        int x = 5, y = 10;

        System.out.println("x == y? " + (x == y)); // false
        System.out.println("x != y? " + (x != y)); // true
        System.out.println("x > y? " + (x > y));   // false
        System.out.println("x < y? " + (x < y));   // true
        System.out.println("x >= 5? " + (x >= 5)); // true
        System.out.println("y <= 10? " + (y <= 10)); // true
    }
}